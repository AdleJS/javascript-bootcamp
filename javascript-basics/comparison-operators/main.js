"use strict";

// comparison operators
console.log(2 > 1);
console.log(2 >= 2);
console.log(1 < 0);
console.log(1 <= 0);
console.log(1 == 1);
console.log(1 != 2);

// string comparison
console.log("a" > "b");
console.log("A" > "a");

// type comparisons
console.log('0' == 0);
console.log('2' > 1);

// strict equal sign
console.log('0' === 0);

// null and undefined
console.log(null == undefined);
console.log(null === undefined);